    private void loadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loadMouseClicked
         try {
            Datos c = control.load("DataBase.xml");
            control.agregarTodos(c.getProductos());
            Table_Productos.setModel(new ProductoTableModel(c.getProductos()));
        } catch (Exception ex) {
            Logger.getLogger(ProductoView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_loadMouseClicked

